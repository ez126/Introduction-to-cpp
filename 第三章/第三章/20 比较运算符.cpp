#include <iostream>
using namespace std;
#include <string>

int main5() {

	//比较运算符
	// ==
	int a = 10;
	int b = 20;

	cout << (a == b) << endl;//要加个括号“（）”表示一个优先级先算括号内的


	// ！=

	cout << (a != b) << endl;

	// >

	cout << (a > b) << endl;

	// <

	cout << (a < b) << endl;


	//  >=

	cout << (a >= b) << endl;


	//  <=

	cout << (a <= b) << endl;




	system("pause");
	return 0;
}