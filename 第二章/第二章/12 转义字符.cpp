#include <iostream>
using namespace std;

int main() {

	//转义字符
	 
	//换行符  \n
	 
	cout << " helloworld\n ";

	//反斜杠  \\

	cout << "\\" << endl;//两个\\输出一个\
		
	//水平制表符 \t 可以让输出的看着整齐一点

	cout << "aaa\t\thelloworld" << endl;
	cout << "aa\thelloworld" << endl;
	cout << "aaaaaa\thelloworld" << endl;


	system("pause");
	return 0;
}